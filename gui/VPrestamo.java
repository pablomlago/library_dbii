    private void btnPrestarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrestarLibroActionPerformed
        // TODO add your handling code here:
        ModeloTablaPrestamos mp;
        mp = (ModeloTablaPrestamos) tablaUsuarios.getModel();
        
        Usuario usuario = null;
        
        if(mp.getRowCount() != 0)
        {
            usuario = mp.obtenerUsuario(tablaUsuarios.getSelectedRow());
            
            //Donde meter esta comprobacion?
            if(usuario.getPrestamosVencidos() == 0)
            {
                if(this.fa.prestarEjemplar(usuario.getIdUsuario(), idLibro, numEjemplar))
                {
                    salirVentana();
                }
            }
            else
            {
                this.fa.muestraExcepcion("O usuario ten prestamos vencidos!");
            }
        }
    }//GEN-LAST:event_btnPrestarLibroActionPerformed


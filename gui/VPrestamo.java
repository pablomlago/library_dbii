    public void buscarUsuarios(){
        ModeloTablaPrestamos m;

        m=(ModeloTablaPrestamos)tablaUsuarios.getModel();
        
        m.setFilas(fa.obtenerUsuariosPrestamos(buscaId.getText(), buscaNombre.getText()));
        if (m.getRowCount() > 0) {
            tablaUsuarios.setRowSelectionInterval(0, 0);
            
            actualizarEstadoBotons(0);
        }
    }
    
    public void salirVentana()
    {
        ((VLibro)super.getParent()).actualizarEjemplares();
        this.setVisible(false);
        this.dispose();
    }
    
    public void actualizarEstadoBotons(int i)
    {
        ModeloTablaPrestamos m = (ModeloTablaPrestamos)tablaUsuarios.getModel();
        
        if(m.getRowCount() > 0)
        {
            tablaUsuarios.setRowSelectionInterval(i,i);
            
            if(m.obtenerUsuario(i).getPrestamosVencidos() > 0)
            {
                btnPrestarLibro.setEnabled(false);
            }
            else
            {
                btnPrestarLibro.setEnabled(true);
            }
        }
        else
        {
            btnPrestarLibro.setEnabled(false);
        }
    }
